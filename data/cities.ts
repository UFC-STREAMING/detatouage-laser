// Helper function to get city by slug
export function getCityBySlug(slug: string): City | undefined {
  return cities.find((city) => city.slug === slug);
}

// Helper function to get cities by department
export function getCitiesByDepartment(departmentSlug: string): City[] {
  return cities.filter((city) => city.department.slug === departmentSlug);
}

// Helper function to get all unique departments
export function getAllDepartments(): Department[] {
  const departmentMap = new Map<string, Department>();

  cities.forEach((city) => {
    const deptSlug = city.department.slug;
    if (!departmentMap.has(deptSlug)) {
      departmentMap.set(deptSlug, {
        name: city.department.name,
        number: city.department.number,
        slug: city.department.slug,
        cities: [],
      });
    }
    departmentMap.get(deptSlug)!.cities.push(city);
  });

  return Array.from(departmentMap.values()).sort((a, b) => a.name.localeCompare(b.name));
}

// Helper function to get proximity cities
export function getProximityCities(citySlug: string): City[] {
  const city = getCityBySlug(citySlug);
  if (!city || !city.proximity) return [];

  return city.proximity
    .map((slug) => getCityBySlug(slug))
    .filter((c): c is City => c !== undefined);
}
